Str = "Python is the king."

print(len(Str))                     # Provide the length of string
print(Str.strip())                  # Remove white spaces from forward and backward of string
print(Str.lower())                  # LowerCase
print(Str.upper())                  # UpperCase
print(Str.replace("Python", "AI"))  # Replace the string
print(Str.startswith("Py"))         # True
print(Str.endswith("ng."))          # True
print(Str.capitalize())             # Make first character uppercase
print(Str.title())                  # Make all first character which is before white space uppercase
