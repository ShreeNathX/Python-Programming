"""
Used for comparision and it return boolean values
"""
x = 10
y = 10

print(f'x is equal to y: {x == y}')
print(f'x is not equal to y: {x != y}')
print(f'x is greater than y: {x > y}')
print(f'x is less than y: {x < y}')
print(f'x is greater than and equal to y: {x >= y}')
print(f'x is smaller than and equal to y: {x <= y}')